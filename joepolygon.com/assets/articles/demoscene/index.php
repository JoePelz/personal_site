    <h1>The Demoscene</h1>
    <p>Interestingly, an anagram for "Encodes Theme"</p>
  </div>
  <div class="content">
    <div class="center"><img src="/assets/articles/demoscene/link.jpg"></div>
    <p>Wikipedia's article on the <a href="https://en.wikipedia.org/wiki/Demoscene">demoscene</a> provides the following description:</p>
    <blockquote>The demoscene is an international computer art subculture that specializes in producing demos: small, self-contained computer programs that produce audio-visual presentations. The main goal of a demo is to show off programming, artistic, and musical skills.</blockquote>
    <p>It's bad style to cite wikipedia, but no additional citation is available for that part. </p>
    <p>What I really love about the demos is that they're not pre-rendered movies, or video playing back. At least, not wholly.  It's all programmed by people, and when you run it, your computer is creating and rendering it all in real time.  Demos are a direct collaboration between science and art.  It boggles my mind how many things have to come together for a demo to run and be complete.</p>
    <p>It's definitely a subculture and pretty niche, but there are gatherings and competitions around the world to show peoples' and teams' latest creations. My main resource for finding demos is through <a href="http://www.pouet.net">Pouet.net</a> and looking at the results from various competitions around the world like Assembly and Revision.</p>
    <p>Like art in general there are different types of demos, many of which have nostalgic appeal to people who aren't me. You like some and don't like some. I have too many favorites to list here, but I've included YouTube links for the four demos iconified above.</p>
